		SETB P2.0
		MOV A,#66H
	
	CW:	JNB P2.0,ACW	;SWITCH IS CONNECTED TO P2.0
		MOV P1,A
		RR A			;MOTOR ROTATES CLOCKWISE
		ACALL DELAY
		SJMP CW
	ACW:MOV P1,A
		RL A			;MOTOR ROTATES ANTICLOCKWISE
		ACALL DELAY
		SJMP CW
  
  
  
  DELAY:MOV R2,#100		;DELAY PROGRAM
  L1:	MOV R3,#255
  L2:	DJNZ R3,L2
		DJNZ R2,L1
